#include <push_swap.h>
#include <stdlib.h>

void
	throw_error(
int status
)
{
	exit(status);
}

void
	cleanup(
t_dlist *head
)
{
	t_dlist	*current;
	t_dlist	*next;

	current = head;
	while (current && current->next != head)
	{
		next = current->next;
		free(current);
		current = next;
	}
	if (current)
		free(current);
}
